package messeges;

public class Messege {

	//fields:
	private String posterName;
	private String text;
	private String time;

	public Messege(String posterName, String text, String time) {
		super();
		this.posterName = posterName;
		this.text = text;
		this.time = time;
	}

	public String getPosterName() {
		return posterName;
	}
	
	public String getText() {
		return text;
	}
	
//	public Messeges

	
}
